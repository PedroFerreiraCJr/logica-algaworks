package br.com.algaworks.curso_logica._03_variaveis_e_constantes;

public class TiposNumericos {
	public static void main(String[] args) {
		Byte numeroByte = 127;			// -128 à 127
		Short numeroShort = 32_767;			// -32768 à 32767
		Integer numeroInteger = 2_147_483_647;		// -2.147.483.648 à 2.147.483.647
		Long numeroLong = 1L;			// -9,223372037×10¹⁸ à 9,223372037×10¹⁸-1
		
		Float numeroFloat = 1.0F;
		Double numeroDouble = 1.0;
	}
}
